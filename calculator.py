a=int(input("enter the first no.:- "))
b=int(input("enter the second no.:- "))
opr=input("enter the operator......(+,/,*,-,%)")
if opr=="+":
    print(a+b)
elif opr=="+":
    print(a+b)
elif opr=="-":
    print(a-b)
elif opr=="*":
    print(a*b)
elif opr=="%":
    print(a%b)
else:
    print("Invalid number")